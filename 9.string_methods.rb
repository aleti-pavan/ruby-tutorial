=begin
Ruby also provides methods from the string class to manipulate strings. 

These methods can be called using the . (dot) operator after string. 

A few of these methods are length, reverse, upcase, and downcase.

=end

string = "This is Ruby Tutorials"

puts "Length of #{string} : #{string.length}"
puts string.length

puts "Reverse of #{string} : #{string.reverse}"
puts string.reverse

puts "Uppercase of #{string} : #{string.upcase}"
puts string.upcase

puts "Lowercase of #{string} : #{string.downcase}"
puts string.downcase
